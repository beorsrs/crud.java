package model;


public class SementeModel {
    
    private int id_semente;
    private String nome_semente, peso_semente;

    public int getId_semente() {
        return id_semente;
    }

    public void setId_semente(int id_semente) {
        this.id_semente = id_semente;
    }

    public String getNome_semente() {
        return nome_semente;
    }

    public void setNome_semente(String nome_semente) {
        this.nome_semente = nome_semente;
    }

    public String getPeso_semente() {
        return peso_semente;
    }

    public void setPeso_semente(String peso_semente) {
        this.peso_semente = peso_semente;
    }


}
